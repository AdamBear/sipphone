#ifndef __MOD_TEST_MODE_H__
#define __MOD_TEST_MODE_H__

#include <ETLLib.hpp>

/************************************************************************/
/* 测试模式模块对外的接口                                               */
/************************************************************************/

/*******************************************************************
** 函数描述:    初始化测试模式
** 参数：        无
** 返回:          无
********************************************************************/
void testmode_Init();

/*******************************************************************
** 函数描述:    判断当前是否处于测试模式
** 参数：        无
** 返回:          true:处于测试模式中,false:不处于测试模式中
********************************************************************/
bool testmode_IsTestMode();

/*******************************************************************
** 函数描述:    进入测试模式
** 参数：        bFullVoice 是否是整机免提音质测试模式
** 返回:      无
********************************************************************/
void testmode_Enter(int iTestType);

/*******************************************************************
** 函数描述:    退出测试模式
** 参数：
** 返回:      无
********************************************************************/
void testmode_Exit();

#endif
